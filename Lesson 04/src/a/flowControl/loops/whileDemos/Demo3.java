package a.flowControl.loops.whileDemos;

public class Demo3 {

	public static void main(String[] args) {
		/*
		 * print your name 10 times
		 */

		int c = 1; // counter

		while (c <= 10) {
			System.out.println("Eldar");
			c++; // c = c + 1;
		}

		System.out.println("=========");
	}
}
