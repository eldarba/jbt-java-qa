package c.dataTypes;

public class Tar1 {
	/*
	 * כתבו תכנית שמצהירה על הנתונים הבאים:
	 * 
	 * המספר 52
	 * 
	 * המספר 3.56 סכום שני המספרים
	 * 
	 * יש להדפיס את הסכום
	 */
	public static void main(String[] args) {

		int x = 52;
		double y = 3.56;
		double sum = x + y; 
		
		System.out.print("sum of ");
		System.out.print(x);
		System.out.print(" and ");
		System.out.print(y);
		System.out.print(" is ");
		System.out.println(sum);
		
	}
}
